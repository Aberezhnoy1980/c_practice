#include <stdio.h>
#include <string.h> // для strlen

int main()
{
    char n[33];
    int vowels = 0;     // счетчик гласных
    int consonants = 0; // счетчик согласных

    printf("Введите строку -> ");
    // fgets читает до 32 символов + место для '\0'
    if (fgets(n, sizeof(n), stdin) == NULL)
    {
        printf("Ошибка ввода.\n");
        return 1;
    }

    // Удаляем символ новой строки '\n', если он есть
    // fgets гарантирует, что строка завершена '\0', так что мы можем это сделать
    size_t len = strlen(n);
    if (len > 0 && n[len - 1] == '\n')
    {
        n[len - 1] = '\0';
        len--; // обновляем длину
    }

    // Проходим по строке до терминатора
    for (size_t i = 0; n[i] != '\0'; i++)
    {
        unsigned char d = n[i]; // используем unsigned char для кодов > 127

        // Проверяем, что это строчная русская буква
        if ((d >= 'а' && d <= 'я') || d == 'ё')
        {
            // Проверяем, является ли буква гласной
            if (d == 'а' || d == 'у' || d == 'о' || d == 'ы' || d == 'э' ||
                d == 'е' || d == 'ё' || d == 'и' || d == 'ю' || d == 'я')
            {
                vowels++;
            }
            else
            {
                // Если это русская буква, но не гласная - значит, согласная
                consonants++;
            }
        }
        // ВСЕ, что не является строчной русской буквой, мы игнорируем
        // (пробелы, знаки препинания, цифры, латиница и т.д.)
    }

    // Вывод результата
    if (vowels > consonants)
    {
        printf("Гласных букв больше, чем согласных\n");
    }
    else if (vowels < consonants)
    {
        printf("Согласных букв больше, чем гласных\n");
    }
    else
    {
        printf("Гласных и согласных букв равное количество\n");
    }

    return 0;
}